									I am a young lady with 13years of working experience 6 of which has been in the IT field.
I have always been dreaming of being an entrepreneur but had not yet gotten the courage to do so until last April when I finally decided to quit my job and prepare myself for this dream.
I had planned to enter into Networking, Website & mobile App development and was thinking of where to go for this training. Upon speaking to one of my IT mentors, he introduced me to Mobile Web Ghana and gave me the contact of the Director (Florence).
At my first call, Florence mentioned this planned program (FiME) to me and asked if I was interested.
Though interested, I still pursued a 2week refresher program with Mobile Web Ghana.
I applied for the FiME program, went through the as called for an interview, and pretty enjoying the program so far.
The program started with an introduction to HTML and JavaScript. We continued with Mobile Value Added Services.
By the end of the second week I was able to develop a mobile App named “To Do List” on my mobile phone using phoneGap and thanks to Selorm Banybah who made the session so interesting for us.
Though I missed the UI/UX session since I was indisposed, the training material was so clear and self explanatory I could gather some idea on it.
We are currently enjoying Saviour Kirk Agbenyegah who is taking us through Google Cloud Platform using Github.
</br>
To attain success, one would have to be committed to crossing all the hurdles that come our way.
I must confess the timing of the training has not been favorable for me though, however I’m committed to getting the best out of it and to break the ice to seeing a dream come true.
The only major problem is about internet connectivity which mostly slowed us down during the training. 
Florence (Director), we are grateful for this idea to empower and train more females in entrepreneurship and mobile apps development. You are a GENIUS!
Adams (Project Coordinator) has always been ready for us be it questions, support etc…
</br>
FiME has been an eye-opener and the preparation stage of a dream come true. 
Big ups to MOBILE WEB GHANA!!!.
 
									
